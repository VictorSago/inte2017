package dsv.inte2017g11.roguelib.Items;

/**
 * enum class that's used for controlling what attribute of a game-character
 * that is effected by the item
 * Health - gives the character more health/armor
 * Magic - affects the characters magic
 * Speed - affects the characters speed
 * Damage - affects how the character deals damage (power points)
 **/
public enum Effect {
    HEALTH,
    MAGIC,
    SPEED,
    DAMAGE
}
